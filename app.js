//my Variables
var a = 9;
var b = 6;


//Addition Orginal Function  +
function addSum(param1, param2){
    console.log(param1 + param2);}
addSum(a, b);
//Addition Arrow Function   +
const AddSum = (param1, param2) => {
    console.log(param1 + param2);}
AddSum(a,b);




//Subtractraction Original Function   -
function subtractSum(param1, param2) {
    console.log(param1 - param2);}
subtractSum(a,b);
//Subtractraction Arrow Function   -
const SubtractSum =(param1, param2)=> {
    console.log(param1 - param2);}
    SubtractSum(a,b);




